def zaputin_transform(text: str) -> str:
    """械芯斜邪蟹械 械泻 胁 懈谢 械卸懈屑邪 zaputin"""
    if not text:
        return text
        
    replacements = {
        '': 'Z', '蟹': 'Z',
        '': 'V', '胁': 'V',
        '小': 'Z', '': 'Z', 
        '': 'ZZ', '小小': 'ZZ',
        '小': 'Zz', '小': 'zZ'
    }
    
    for old, new in replacements.items():
        text = text.replace(old, new)
    
    return text

