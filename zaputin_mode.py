import random

def zaputin_transform(text: str) -> str:
    """Преобразует текст в стиль режима zaputin"""
    if not text:
        return text
        
    # Заменяем символы (все варианты)
    replacements = {
        'З': 'Z', 'з': 'Z',
        'В': 'V', 'в': 'Z',
        'С': 'Z', 'с': 'Z', 
        'сс': 'ZZ', 'СС': 'ZZ',
        'Сс': 'Zz', 'сС': 'zZ'
    }
    
    for old, new in replacements.items():
        text = text.replace(old, new)
    
    return text

