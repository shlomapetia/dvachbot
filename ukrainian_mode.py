def ukrainian_transform(text: str) -> str:
    """Преобразует русский текст в украинский вариант с вариативностью"""
    if not text:
        return text

    lines = text.split('\n')
    transformed_lines = []

    for line in lines:
        words = line.split()
        transformed_words = []

        for word in words:
            punctuation = ""
            if word and word[-1] in ',.!?;:':
                punctuation = word[-1]
                word = word[:-1]

            is_upper = word.isupper()
            is_title = word.istitle()
            base_word = word.lower()

            replacement = UKRAINIAN_WORD_REPLACEMENTS.get(base_word)
            if replacement:
                if isinstance(replacement, list):
                    new_word = random.choice(replacement)
                else:
                    new_word = replacement

                if is_upper:
                    new_word = new_word.upper()
                elif is_title:
                    new_word = new_word.capitalize()
            else:
                new_word = word
                if 'и' in new_word.lower():
                    rand = random.random()
                    if rand < 0.45: new_word = new_word.replace('и', 'ї').replace('И', 'Ї')
                    elif rand < 0.9: new_word = new_word.replace('и', 'i').replace('И', 'I')
                new_word = new_word.replace('ы', 'i').replace('Ы', 'I')
                new_word = new_word.replace('е', 'є').replace('Е', 'Є')
                new_word = new_word.replace('э', 'є').replace('Э', 'Є')
                if random.random() < 0.3: # С вероятностью 30% заменяем 'г'
                    new_word = new_word.replace('г', 'ґ').replace('Г', 'Ґ')
                new_word = new_word.replace('тся', 'ться')

            transformed_words.append(new_word + punctuation)

        transformed_lines.append(' '.join(transformed_words))

    return '\n'.join(transformed_lines)

